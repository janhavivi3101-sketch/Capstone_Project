// converts "LoginNegativeV2Test" → "loginNegativeV2Test" for method names
function toCamel(s) {
  return s.charAt(0).toLowerCase() + s.slice(1);
}

// ── TOOL: execute_tests ───────────────────────────────────────────────────────
function executeTests(args) {
  const { filter, suite, group, headless = true } = args || {};
  let cmd = `${MVN} clean test`;
  if (suite)    cmd += ` -Dsurefire.suiteXmlFiles=src/test/resources/${suite}`;
  if (filter)   cmd += ` -Dtest=${filter}`;
  if (group)    cmd += ` -Dgroups=${group}`;
  if (headless) cmd += ` -Dheadless=true`;
  cmd += ` -q`;
  const result = runCommand(cmd);
  return `Command: ${cmd}\n\nSuccess: ${result.success}\n\n${result.output.slice(0, 8000)}`;
}

// ── TOOL: get_test_results ────────────────────────────────────────────────────
function getTestResults(args) {
  const { format = "summary" } = args || {};
  const surefireDir = path.join(PROJECT_ROOT, "target", "surefire-reports");
  const allureDir   = path.join(PROJECT_ROOT, "target", "allure-results");
  let output = "=== Test Results ===\n\n";

  if (fs.existsSync(surefireDir)) {
    try {
      const files = fs.readdirSync(surefireDir).filter(f => f.endsWith(".xml"));
      let passed = 0, failed = 0, skipped = 0, errors = 0;
      for (const f of files) {
        const c = safeRead(path.join(surefireDir, f));
        const tests   = parseInt(c.match(/tests="(\d+)"/)?.[1]    || 0);
        const fa      = parseInt(c.match(/failures="(\d+)"/)?.[1] || 0);
        const er      = parseInt(c.match(/errors="(\d+)"/)?.[1]   || 0);
        const sk      = parseInt(c.match(/skipped="(\d+)"/)?.[1]  || 0);
        passed  += tests - fa - er - sk;
        failed  += fa;
        errors  += er;
        skipped += sk;
      }
      output += `Surefire Summary\n  Passed : ${passed}\n  Failed : ${failed + errors}\n  Skipped: ${skipped}\n\n`;
      if (format === "full") {
        for (const f of files.slice(0, 10)) {
          output += `--- ${f} ---\n${safeRead(path.join(surefireDir, f)).slice(0, 2000)}\n\n`;
        }
      }
    } catch (e) { output += `Surefire read error: ${e.message}\n`; }
  } else {
    output += "No surefire-reports found. Run tests first.\n";
  }

  if (fs.existsSync(allureDir)) {
    const count = fs.readdirSync(allureDir).filter(f => f.endsWith("-result.json")).length;
    output += `Allure result files: ${count}\n  Path: ${allureDir}\n  Run: mvn allure:serve  to open\n`;
  }
  return output;
}

// ── TOOL: list_project_structure ──────────────────────────────────────────────
function listProjectStructure(args) {
  const { ext = "", dir: subDir = "" } = args || {};
  const searchDir = subDir ? path.join(PROJECT_ROOT, subDir) : PROJECT_ROOT;
  const files = walkDir(searchDir, ext);
  return files.map(f => f.replace(PROJECT_ROOT, "").replace(/\\/g, "/")).join("\n") || "No files found.";
}

// ── TOOL: read_file ───────────────────────────────────────────────────────────
function readFile(args) {
  if (!args?.file_path) return "ERROR: file_path is required";
  const full = path.isAbsolute(args.file_path)
    ? args.file_path
    : path.join(PROJECT_ROOT, args.file_path);
  return safeRead(full);
}

// ── TOOL: write_file ──────────────────────────────────────────────────────────
function writeFile(args) {
  if (!args?.file_path) return "ERROR: file_path is required";
  if (args.content === undefined) return "ERROR: content is required";
  const full = path.isAbsolute(args.file_path)
    ? args.file_path
    : path.join(PROJECT_ROOT, args.file_path);
  return safeWrite(full, args.content);
}

// ── TOOL: list_test_classes ───────────────────────────────────────────────────
function listTestClasses() {
  const testDir = path.join(PROJECT_ROOT, "src", "test", "java");
  const files = walkDir(testDir, ".java");
  return files
    .filter(f => f.includes("Test"))
    .map(f => f.replace(PROJECT_ROOT, "").replace(/\\/g, "/"))
    .join("\n") || "No test classes found.";
}

// ── REST ENDPOINT: /suggest-locator ──────────────────────────────────────────
// Called by McpSeleniumClient.java when SelfHealingLocator runs out of fallbacks.
// Receives the failed locator + page HTML and returns a CSS/XPath suggestion.
function buildLocatorSuggestion(failedLocator, pageHtml) {
  // Simple heuristic: look for common patterns near the failed locator's hint
  // In a real integration Claude Desktop would answer this — here we provide
  // deterministic fallbacks so tests work even without Claude Desktop open.

  // Extract the attribute value from the failed locator string
  // e.g. "By.id: email"  →  "email"
  const hint = failedLocator.replace(/^By\.\w+:\s*/i, "").replace(/['"]/g, "").trim();

  // Try to find elements in the HTML whose id, name, or class contains the hint
  const candidates = [];

  // id= matches
  const idRe = new RegExp(`id=["']([^"']*${hint}[^"']*)["']`, "gi");
  let m;
  while ((m = idRe.exec(pageHtml)) !== null) candidates.push(`id::${m[1]}`);

  // name= matches
  const nameRe = new RegExp(`name=["']([^"']*${hint}[^"']*)["']`, "gi");
  while ((m = nameRe.exec(pageHtml)) !== null) candidates.push(`css::[name="${m[1]}"]`);

  // data-testid= matches
  const dtRe = new RegExp(`data-testid=["']([^"']*${hint}[^"']*)["']`, "gi");
  while ((m = dtRe.exec(pageHtml)) !== null) candidates.push(`css::[data-testid="${m[1]}"]`);

  if (candidates.length > 0) {
    process.stderr.write(`[suggest-locator] Hint "${hint}" → ${candidates[0]}\n`);
    return candidates[0];
  }

  process.stderr.write(`[suggest-locator] No candidate found for hint "${hint}"\n`);
  return null;
}

const healingServer = http.createServer((req, res) => {
  if (req.method === "POST" && req.url === "/suggest-locator") {
    let body = "";
    req.on("data", chunk => { body += chunk; });
    req.on("end", () => {
      try {
        const { failedLocator = "", pageHtml = "" } = JSON.parse(body);
        const suggestion = buildLocatorSuggestion(failedLocator, pageHtml);
        res.writeHead(200, { "Content-Type": "application/json" });
        res.end(JSON.stringify({ suggestion: suggestion || "" }));
      } catch (e) {
        res.writeHead(400, { "Content-Type": "application/json" });
        res.end(JSON.stringify({ error: e.message }));
      }
    });
  } else if (req.method === "GET" && req.url === "/health") {
    res.writeHead(200);
    res.end("ok");
  } else {
    res.writeHead(404);
    res.end("not found");
  }
});

healingServer.listen(HEALING_PORT, "127.0.0.1", () => {
  process.stderr.write(`[REST] Self-healing endpoint ready on http://127.0.0.1:${HEALING_PORT}/suggest-locator\n`);
});

