// ── HELPERS ───────────────────────────────────────────────────────────────────

function safeRead(filePath) {
  try { return fs.readFileSync(filePath, "utf8"); }
  catch (e) { return `ERROR reading file: ${e.message}`; }
}

function safeWrite(filePath, content) {
  try {
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, content, "utf8");
    return `Written: ${filePath}`;
  } catch (e) { return `ERROR writing file: ${e.message}`; }
}

function walkDir(dir, ext = "", results = [], depth = 0) {
  if (depth > 8) return results;
  try {
    for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
      if (["node_modules", ".git", "target", ".idea"].includes(e.name)) continue;
      const full = path.join(dir, e.name);
      if (e.isDirectory()) walkDir(full, ext, results, depth + 1);
      else if (!ext || e.name.endsWith(ext)) results.push(full);
    }
  } catch {}
  return results;
}

function runCommand(cmd, cwd) {
  try {
    const out = execSync(cmd, {
      cwd: cwd || PROJECT_ROOT,
      timeout: 300_000,
      encoding: "utf8",
      stdio: ["pipe", "pipe", "pipe"],
    });
    return { success: true, output: out };
  } catch (e) {
    return {
      success: false,
      output: (e.stdout || "") + "\n" + (e.stderr || "") + "\n" + e.message,
    };
  }
}

