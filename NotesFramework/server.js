#!/usr/bin/env node

/**
 * Capstone MCP Server — HybridNotesFramework Edition
 * ====================================================
 * Connects Claude Desktop to your Selenium Java project.
 *
 * Tools exposed to Claude Desktop:
 *   1. list_project_structure   — shows all files in the project
 *   2. read_file                — reads any file by path
 *   3. write_file               — creates or overwrites a file
 *   4. generate_test_script     — generates a full Java TestNG class from plain English
 *   5. execute_tests            — runs mvn test with filters / groups / suite XML
 *   6. get_test_results         — reads Surefire + Allure results after a run
 *   7. list_test_classes        — lists all test classes in src/test/java
 *
 * ALSO exposes a plain HTTP REST endpoint (not an MCP tool) used by
 * SelfHealingLocator.java inside the Java tests:
 *   POST http://localhost:3000/suggest-locator
 *   Body:  { "failedLocator": "...", "pageHtml": "..." }
 *   Reply: { "suggestion": "css:: .btn-add" }
 *
 * The MCP protocol side talks over stdio (Claude Desktop connects to it).
 * The REST side listens on port 3000 (the Java tests connect to it).
 * Both run inside the same Node process.
 */

